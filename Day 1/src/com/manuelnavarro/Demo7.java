package com.manuelnavarro;

public class Demo7 {

    public static void main(String[] args) {

        int number = 4;
        switch (number) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            case 4:
                System.out.println("four");
                break;
            default:
                System.out.println("not a valid number");
        }
    }
}
